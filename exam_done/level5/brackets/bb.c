#include "brackets.h"

int		bb(char *a, int *j)
{
	(*j)++;
	while (a[*j])
	{
		if (a[*j] == '}')
			return (1);
		if (a[*j] == ')' || a[*j] == ']')
			return (0);
		if (a[*j] == '(' && !rb(a, j))
			return (0);
		if (a[*j] == '[' && !sb(a, j))
			return (0);
		if (a[*j] == '{' && !bb(a, j))
			return (0);
		(*j)++;
	}
	return (0);
}
