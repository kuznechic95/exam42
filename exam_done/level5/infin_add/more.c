#include "infin_add.h"

int		more(char *num1, char *num2, size_t len1, size_t len2)
{
	if (len1 > len2)
		return (1);
	if (len2 > len1)
		return (0);
	while (*num1 == *num2 && *num1)
	{
		num1++;
		num2++;
	}
	return (*num1 - *num2 > 0 ? 1 : 0);
}
