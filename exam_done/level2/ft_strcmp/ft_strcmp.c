int		ft_strcmp(char *s1, char *s2)
{
	unsigned long long	i;

	i = 0;
	while (*(s1 + i) == *(s2 + i) && *(s1 + i))
		i++;
	return ((int)(*((unsigned char *)s1 + i) - (*(unsigned char *)s2 + i)));
}
