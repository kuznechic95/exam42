#include <unistd.h>

void	print_bits(unsigned char octet)
{
	unsigned char x;

	x = 128;
	while (x > 0)
	{
		if (x & octet)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		x >>= 1;
	}
}
