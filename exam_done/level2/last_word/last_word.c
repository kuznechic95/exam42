#include <unistd.h>

void	ft_lw(char *s)
{
	unsigned int i;
	unsigned int j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (s[i] == ' ' && s[i + 1] >= 33 && s[i + 1] < 127)
			j = i + 1;
		i++;
	}
	while (s[j] >= 33 && s[j] < 127)
	{
		write(1, &s[j], 1);
		j++;
	}
}

int		main(int ac, char **av)
{
	if (ac == 2)
	{
		ft_lw(av[1]);
	}
	write(1, "\n", 1);
}
